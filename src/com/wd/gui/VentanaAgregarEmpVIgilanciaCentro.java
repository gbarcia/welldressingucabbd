    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int indiceCentro = this.centros.getSelectedIndex();
        int indiceEmp = this.empresas.getSelectedIndex();

        Vector<EmpresaVigilancia> vecEmpVig = new Vector(this.empresasVigilancia);
        EmpresaVigilancia ev = vecEmpVig.get(indiceEmp);
        String rifEmp = ev.getRif();
        
         Vector<CentroDistribucion> vecCentro = new Vector(this.centrosDistribucion);
        CentroDistribucion cd = vecCentro.get(indiceCentro);
        int codigoEmpresa = cd.getCodigo();

        this.control = new ControlGuiAgregarVigilanciaCentro();
        this.control.agregarActualizarVigilanciaCentro(rifEmp, codigoEmpresa);

    }//GEN-LAST:event_jButton1ActionPerformed


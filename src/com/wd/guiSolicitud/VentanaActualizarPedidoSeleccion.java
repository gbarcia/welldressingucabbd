    public VentanaActualizarPedidoSeleccion() {
        initComponents();
        pedidos = control.traerTodosLosPedidos();

        DefaultTableModel dm1 = new DefaultTableModel();
        dm1.addColumn("Id");
        dm1.addColumn("Emicion");
        dm1.addColumn("Estimada");
        dm1.addColumn("Estado");
        dm1.addColumn("Centro");
        dm1.addColumn("Tienda");


        for (Pedido o : this.pedidos) {
            Vector info = new Vector();
            info.addElement(o.getId());
            info.addElement(o.getFechaGenerada());
            info.addElement(o.getFechaEstimada().toString());
            info.addElement(this.traducirEstado(o.getStatus()));
            info.addElement(o.getNombreCebtro());
            info.addElement(o.getNombreTienda());
            dm1.addRow(info);
        }
        this.tablaOrdenes.setModel(dm1);
    }

    private String traducirEstado (int edo) {
        switch (edo) {
            case 0: return "APROBADO";
            case 1: return "DESPACHADO";
            case 2: return "RECIBIDO";
            case 3: return "CANCELADO";
            default: return "DESCONOCIDO";
        }
    }

